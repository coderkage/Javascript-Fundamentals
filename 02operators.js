/* arithmatic operators */

let a = 2+3; //add
let b = 4-7; //sub
let c = 5*4; //mul
let d = 8/3; //div
let e = 2**3; //exp
let f = 10%4; //mod

console.log(a,b,c,d,e,f);

/* assignment operators */

let x = 10;
x+=9;
x-=2;
x/=4;
x%=8;
x*=5;
x**=4;

console.log(x);

/* comparision operators */

console.log(1==2);
console.log(1!=2);
console.log(1>2);
console.log(1<2);
console.log(1>=2);
console.log(1<=2);
console.log(1===2);

console.log(3=="3"); //equality operator
